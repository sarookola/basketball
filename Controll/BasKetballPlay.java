    private void loadbaskControldataFile() {
        basketControllData = (BasketControllData) new FileManipulat().loadBasketControllData(basketControllDataFilename);
        jPanelQuarterName.removeAll();
        for (int i = 0; i < basketControllData.getQmax(); i++) {
            jPanelQuarterName.add(new JLabel(basketControllData.getQuName(i)));
        }
        jToggleButtonShowTeamNameA.setSelected(basketControllData.getShowTeamName()[0]);
        jToggleButtonShowTeamNameB.setSelected(basketControllData.getShowTeamName()[1]);
//        jTextField_point_1.setText(basketControllData.);
        updateUI();
    }

    private void savvebaskControldataFile() {

        new FileManipulat().saveObject(basketControllDataFilename, basketControllData);

    }

    private void renameQuarterName() {
        if (buttonGroupQuarter.getSelection() != null) {
            int x = Integer.parseInt((buttonGroupQuarter.getSelection()).getActionCommand());
            String st = JOptionPane.showInputDialog("Enter quarter " + x + " Name :");
            if (st != null) {
                if (!st.equals("")) {
                    basketControllData.setQuName(st, x);
                }
                savvebaskControldataFile();
                loadbaskControldataFile();
            } else {
                JOptionPane.showMessageDialog(null, "نام معتبری وارد نکردی");
            }
        } else {
            JOptionPane.showMessageDialog(null, "نخست کوارتر را انتخاب کن");
        }
    }

