    private void updatescoreHistory() {
        for (int i = 0; i < scoreHistory.getMaxhistory(); i++) {
            jtextscoreHistory1[i].setText(scoreHistory.getscore1(i));
            jtextscoreHistory2[i].setText(scoreHistory.getscore2(i));
        }
    }

